package com.company;

public class MusicHall {
    private String city;
    private String nameOfHall;

    public MusicHall(String city, String nameOfHall) {
        this.city = city;
        this.nameOfHall = nameOfHall;
    }

    public String getCity() {
        return city;
    }

    public String getNameOfHall() {
        return nameOfHall;
    }
}
